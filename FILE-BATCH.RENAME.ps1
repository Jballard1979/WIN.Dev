#-- ************************************************************************************************************:
#--
#-- ********************************************************:
#-- VERIFY SCRIPT HAS BEEN RAN USING ADMIN PREV:
#-- ********************************************************:
$DIRPath = GET-CHILDITEM -PATH D:\TEMP\*.mp4
#--
#-- LOOG THROUGH DIRECTORY:
foreach ($Item in $DIRPath)
{
	#- DEFINE NEW FILE NAME:
    $NEWName = $Item.Name -REPLACE("_CPD")
    RENAME-ITEM -PATH $item.Name -NEWNAME $NEWName
}

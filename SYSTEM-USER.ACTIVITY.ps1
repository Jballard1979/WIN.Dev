function global:IsUserOnComputer
{
	param($User,$Computer)
	$explorer = Get-WmiObject -ComputerName $computer win32_Process | Where-Object {$_.ProcessName -eq "Explorer.exe"}
	if ($explorer)
	{
		if (($explorer.getowner()).user -eq $User)
		{
			return $TRUE
		}
		else 
		{
			return $FALSE
		}
	}
}
